package JavaBasics;

public class UnaryOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        int c = 15;
        int d = c++;
        System.out.println(c);
        System.out.println(d);
    }
}
