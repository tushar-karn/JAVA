package JavaBasics;

public class VariablesInJava {
    public static void main(String[] args) {
        int a = 25;
        int b = 10;
        System.out.println(a);
        System.out.println(b);
        String name= "Tony Stark";
        System.out.println(name);
        a = 40;
        b = 55;
        System.out.println(a);
        System.out.println(b);
    }
}
