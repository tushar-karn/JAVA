package JavaBasics;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a =10;
        a+=10;
        System.out.println(a);
        a-=5;
        System.out.println(a);
        a*=5;
        System.out.println(a);
        a/=5;
        System.out.println(a);
    }
}
