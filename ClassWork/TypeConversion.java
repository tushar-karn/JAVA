
public class TypeConversion {
    public static void main(String[] args) {
        char a = 't';
        char b = 'u';
        char c = 's';
        char d = 'h';
        char e = 'a';
        char f = 'r';

        System.out.print((char)(a-32));
        System.out.print((char)(b-32));
        System.out.print((char)(c-32));
        System.out.print((char)(d-32));
        System.out.print((char)(e-32));
        System.out.print((char)(f-32));
    }
}